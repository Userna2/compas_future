export function getClubById(id: string): ClubData | undefined {
  return clubItems.find((club) => club.id === id)
}

export function getTotalStats() {
  return {
    participants: clubItems.reduce((acc, club) => acc + club.participants, 0),
    reports: clubItems.reduce((acc, club) => acc + club.reports, 0),
    excursions: clubItems.reduce((acc, club) => acc + club.excursions, 0),
    cities: clubItems.length,
  }
}
